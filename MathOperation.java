package vnrvjiet.sreeja;

public class MathOperation
{
int add(int m,int n)
{
int sum = m + n;
return(sum);
}
int sub(int m,int n)
{
int subans = m - n;
return(subans);
}
int mul(int m,int n)
{
int mulans = m * n;
return(mulans);
}
double div(double m, double n)
{
double divans = (double)m / n;
return(divans);
}
}